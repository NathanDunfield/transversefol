function homology(d_matrix::AbstractArray{T,2}) where {T}
	@assert size(d_matrix,1)==size(d_matrix,2)
	n = size(d_matrix,1)
	@assert d_matrix ^2 == fill(zero(T),(n,n))

	#myprintln(d_matrix)
	L,D,U,Linv,Uinv=LDU(d_matrix)
	#LinearAlgebra.myprintln(D)
	#myprintln(D)
	@assert L*D*U==d_matrix
	@assert U*Uinv==eye(T,n,n)
	@assert Linv*L==eye(T,n,n)

	#We work in the basis given by U
	#The kernel of d is generated by the nonzero elements in mask
	#Each element of U*d_matrix gives a relation among these generators
	mask = Bool[D[i,i]==zero(T) ? true : false for i in 1:n]
	relations = my_times(U,d_matrix)[mask,:]
	m=size(relations,1)
	#myprintln(relations)

	@assert d_matrix*Uinv[:,mask] == zeros(T,n,sum(mask))

	pL,pD,pU,pLinv,pUinv=LDU(relations)
	@assert pU*pUinv==eye(T,n,n)
	@assert pLinv*pL==eye(T,m,m)
	@assert pL*pD*pU==relations

	#finally, the homology is generated by 
	generators = my_times(Uinv[:,mask],pL)
	#myprintln(generators)
	#myprintln(pD)

	indices = filter(i->!is_unit(pD[i,i]), 1:m)
	return diag(pD)[indices], generators[:,indices]
end

function cokernel(p_matrix::AbstractArray{T,2}) where {T}
	L,D,U,Linv,Uinv = LDU(p_matrix)
	#return filter(x->!isunit(x), diag(D))
	return diag(D)
end

