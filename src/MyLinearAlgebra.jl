function is_unit(x::T) where {T}
	return x==one(T) || x==-one(T)
end

function eye(T,m,n)
	@assert m==n
	return Matrix(Diagonal(ones(T,m)))
end

function LDU(A::Array{T,2}) where {T}
	m,n=size(A)
	L,D,U,Linv,Uinv = eye(T,m,m),copy(A),eye(T,n,n), eye(T,m,m), eye(T,n,n)
	LDU!(L,D,U,Linv,Uinv)

	@assert L*D*U==A
	@assert U*Uinv==eye(T,n,n)
	@assert Linv*L==eye(T,m,m)
	for i in 1:m, j in 1:n
		if i!=j
			@assert D[i,j]==zero(T)
		end
	end
	return L,D,U, Linv,Uinv
end

euclidean_norm(x::Polynomial) = degree(x)
euclidean_norm(x::Number) = abs(x)

function *(x::T, A::Array{T}) where {T<:Polynomial}
	return T[x*y for y in A]
end

