#Works only over a Euclidean domain
function LDU!(_L::AbstractMatrix{T},_D::AbstractMatrix{T},_U::AbstractMatrix{T}, _Linv::AbstractMatrix{T}, _Uinv::AbstractMatrix{T}) where {T}
	Big = (T==Int ? typemax(Int) : PosInf)
	L=@view _L[:,:]
	D=@view _D[:,:]
	U=@view _U[:,:]
	Linv=@view _Linv[:,:]
	Uinv=@view _Uinv[:,:]

	while size(D)[1] > 0 && size(D)[2] > 0
		#check=L*D*U
		m,n=size(D)
		if m==0 || n==0
			return nothing
		end

		changed = true

		#@assert Linv*L == eye(T,m,m)
		#@assert U*Uinv == eye(T,n,n)

		while changed == true
			changed = false
			norms = map(x->x != zero(T) ? euclidean_norm(x) : Big, D[1,:])
			min_norm, i = findmin(norms)
			if min_norm < Big
				pivot = D[1,i]

				#@assert L*D*U == check
				for j in 1:n
					if i!= j && D[1,j] != zero(T)
						d,r = divrem(D[1,j], pivot)
						D[:,j] .-= d .* D[:,i]
						U[i,:] .+= d .* U[j,:]

						Uinv[:,j] .-= d .* Uinv[:,i]
						changed=true
					end
					#@assert Linv*L == eye(T,m,m)
					#@assert U*Uinv == eye(T,n,n)
				end

				if i!= 1
					D[:,1], D[:,i] = D[:,i], D[:,1]
					U[1,:], U[i,:] = U[i,:], U[1,:]
					Uinv[:,1], Uinv[:,i] = Uinv[:,i], Uinv[:,1]
					#@assert Linv*L == eye(T,m,m)
					#@assert U*Uinv == eye(T,n,n)
					changed=true
				end
			end

			norms = map(x->x!=zero(T) ? euclidean_norm(x) : Big, D[:,1])
			min_norm, i=findmin(norms)
			if min_norm < Big
				pivot = D[i,1]

				#@assert L*D*U == check
				for j in 1:m
					if i!= j && D[j,1] != zero(T)
						d,r = divrem(D[j,1], pivot)
						D[j,:] .-= d.*D[i,:]
						L[:,i] .+= d.*L[:,j]
						Linv[j,:] .-= d.*Linv[i,:]
						changed=true
						#@assert Linv*L == eye(T,m,m)
						#@assert U*Uinv == eye(T,n,n)
					end
				end

				if i!=1
					D[1,:], D[i,:] = D[i,:], D[1,:]
					L[:,1], L[:,i] = L[:,i], L[:,1]
					Linv[1,:], Linv[i,:] = Linv[i,:], Linv[1,:]
					changed=true
					#@assert Linv*L == eye(T,m,m)
					#@assert U*Uinv == eye(T,n,n)
				end
			end
		end

		L=@view L[:,2:end]
		D=@view D[2:end,2:end]
		U=@view U[2:end,:]
		Linv=@view Linv[2:end,:]
		Uinv=@view Uinv[:,2:end]
		#@assert L*D*U == check
		#LDU!((@view L[:,2:end]), (@view D[2:end,2:end]), (@view U[2:end,:]), (@view Linv[2:end,:]), (@view Uinv[:,2:end]))
		#@assert L*D*U == check
	end
end

function my_times(A,B)
